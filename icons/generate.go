//go:build ignore

package main

import (
	"bytes"
	"fmt"
	"os"
	"path/filepath"
	"strings"
	"text/template"

	"github.com/invopop/gobl/pkg/here"

	"golang.org/x/text/cases"
	"golang.org/x/text/language"
)

const (
	themeDefault   = "default"
	baseThemesPath = "../../popui/icons/themes" // Assume main popui repo is available
)

type icon struct {
	Name string
	Data string
}

func main() {
	if error := generate(); error != nil {
		panic(error)
	}
}

// generate will prepare an icons templ file that contains all of the icons provided
// by popui.
func generate() error {
	tt := here.Doc(`
		// Code generated by popui - DO NOT EDIT.

		package icons

		//lint:file-ignore SA4006 This context is only used if a nested component is present.

		{{ range .Icons }}
		templ {{ .Name }}() {
			<div style="width: 16px; height: 16px;">
				{{ .Data }}
			</div>
		}
		{{ end }}
	`)
	tmpl, err := template.New("base").Parse(tt)
	if err != nil {
		return fmt.Errorf("preparing template: %w", err)
	}

	files, err := os.ReadDir(filepath.Join(baseThemesPath, themeDefault))
	if err != nil {
		return fmt.Errorf("loading directory: %w", err)
	}

	caser := cases.Title(language.English)

	icons := []icon{}
	for _, file := range files {
		if file.IsDir() {
			continue
		}

		// Remove the .svg extension.
		filename := file.Name()[:len(file.Name())-4]
		// Convert to camel case from dashed case
		name := caser.String(strings.ReplaceAll(filename, "-", " "))
		name = strings.ReplaceAll(name, " ", "")
		icon := icon{
			Name: name,
		}

		data, err := os.ReadFile(filepath.Join(baseThemesPath, themeDefault, file.Name()))
		if err != nil {
			return fmt.Errorf("reading icon file %v: %w", file.Name(), err)
		}
		// Remove new lines for embedding, this should be safe as SVG ignores whitespace.
		icon.Data = strings.ReplaceAll(string(data), "\n", "")
		icons = append(icons, icon)
	}

	data := map[string]any{"Icons": icons}

	out := bytes.NewBuffer(nil)
	if err := tmpl.Execute(out, data); err != nil {
		return fmt.Errorf("executing icons template: %w", err)
	}

	f := filepath.Join("icons_list.templ")
	if err := os.WriteFile(f, out.Bytes(), 0644); err != nil {
		return err
	}
	fmt.Printf("Processed %v\n", f)

	if err := generateDocsIconsList(icons); err != nil {
		return fmt.Errorf("generating docs icons list: %w", err)
	}

	return nil
}

func generateDocsIconsList(icons []icon) error {
	tt := here.Doc(`
		// Code generated by popui - DO NOT EDIT.

		package components

		import (
			"github.com/invopop/popui.go/icons"
		)

		templ Icons() {
			<div style="display: grid; grid-template-columns: repeat(auto-fill, minmax(80px, 1fr)); gap: 12px;">
				{{ range .Icons }}@iconCard("{{ .Name }}", icons.{{ .Name }}())
				{{ end }}
			</div>
		}
	`)
	tmpl, err := template.New("docs").Parse(tt)
	if err != nil {
		return fmt.Errorf("preparing docs template: %w", err)
	}

	out := bytes.NewBuffer(nil)
	if err := tmpl.Execute(out, map[string]any{"Icons": icons}); err != nil {
		return fmt.Errorf("executing docs template: %w", err)
	}

	f := filepath.Join("..", "internal", "docs", "components", "icons_list.templ")
	if err := os.WriteFile(f, out.Bytes(), 0644); err != nil {
		return err
	}
	fmt.Printf("Processed %v\n", f)

	return nil
}
